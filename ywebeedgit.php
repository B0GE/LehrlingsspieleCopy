Github - Weshalb?

Versionsverwaltung für Software-Projekte
An einem Software-Projekt arbeiten heutzutage oftmals mehrere, teilweise sogar hunderte oder tausende Entwickler mit.
 Von denen widmet sich jeder einem anderen Teil des Programms, und deren Arbeitsergebnisse müssen irgendwo zusammengeführt werden.
 Natürlich könnte jeder Entwickler seine Änderungen und Neuerungen an eine zentrale Person schicken, und diese kümmert sich dann ausschließlich darum, den erhaltenen Code immer zu aktualisieren.
 Genau diese Tätigkeit lässt sich aber mithilfe einer Versionsverwaltung wie Git automatisieren.
 Und weil das so praktisch ist und gut funktioniert, finden sich inzwischen viele bekannte OpenSource-Projekte auf GitHub wieder

Sämtliche Änderungen von allen Autoren werden gespeichert
Dazu gibt es diverse Client-Applikationen für Git, hauptsächlich in Form von Kommandozeilen-Tools.
 Hiermit können Entwickler ihre Änderungen an einem Projekt zentral einreichen, und GitHub stellt diese Änderungen ausführlich auf der zugehörigen Webseite dar.
Außerdem speichert Git jede Version des Software-Projekts .

Web-Interface, Wiki und Support-System für Entwickler
Prinzipiell ließe sich Git nämlich auch komplett ohne ein Web-Interface oder zentralen Server verwenden.
Aber GitHub hostet nicht nur kostenlos die OpenSource-Projekte der Entwickler, sondern reichert den Funktionsumfang von Git auch zusätzlich an.
Dank grafischer Darstellung im Browser lässt sich der Entwicklungsprozess von Software-Projekten anschaulich darstellen.
 Außerdem kann ein Großteil der Features, zum Beispiel das „Forken“ eines Projekts, auch durch einen Mausklick ausgeführt werden und benötigt keinen Kommandozeilen-Befehl mehr.
